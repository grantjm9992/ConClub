			function submitForm()
			{
				if ( $('#email').val() != "" )
				{
					$.ajax({
						type: "POST",
						url: "Reservation.make",
						headers: { 'X-CSRF-TOKEN': $('meta[name="csrf-token"]').attr('content') },
						data: $('#form').serialize(),
						success: function(data)
						{
							if ( data == "OK" )
							{
								swal("Success", "Your reservation request has been sent. You will receive an email confirmation shortly.", "success");
							}
						}
					});
				}
				else
				{
					swal("Error", "You must enter a valid email address.", "warning");

				}
			}
